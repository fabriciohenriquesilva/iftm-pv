    private void validarCampos() {
        try {
            double valor1 = Double.parseDouble(tfdValor1.getText());
            double valor2 = Double.parseDouble(tfdValor2.getText());

            calculadora.setN1(valor1);
            calculadora.setN2(valor2);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Devem ser informado apenas valores númericos nos campos", "Argumento inválido", JOptionPane.INFORMATION_MESSAGE);
        }
    }


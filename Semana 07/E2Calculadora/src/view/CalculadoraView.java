    private void btnDivisaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDivisaoActionPerformed
        validarCampos();
        
        double valor2 = Double.parseDouble(tfdValor2.getText());

        if (valor2 == 0) {
            JOptionPane.showMessageDialog(this, "O valor do campo 2 da divisão precisa ser diferente de 0", "Argumento inválido", JOptionPane.INFORMATION_MESSAGE);
        } else {
            calculadora.divisao();
            tfdResultado.setText(String.valueOf(calculadora.getResultado()));
        }
    }//GEN-LAST:event_btnDivisaoActionPerformed

